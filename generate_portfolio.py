def build_nav():
    items = [
        ("Home", "#home"),
        ("About", "#about"),
        ("Resume", resume_file),
        ("Contact", "#contact"),
        ("LinkedIn", linkedin),
    ]
    lis = []
    for label, href in items:
        target = ' target="_blank"' if href.startswith("http") or href == resume_file else ""
        lis.append(f'<li><a href="{href}"{target}>{label}</a></li>')
    links_html = "\n        ".join(lis)
    return f"""
<nav>
    <div class="nav-container">
        <a href="#home" class="logo">{name}</a>
        <span class="nav-toggle"><i class="fas fa-bars"></i></span>
        <ul>
        {links_html}
        </ul>
    </div>
</nav>
"""


