def build_scripts():
    return """
<script>
    // toggle card expansion when anywhere on the card is clicked
    document.querySelectorAll('.card').forEach(card => {
        card.addEventListener('click', () => {
            card.classList.toggle('expanded');
        });
    });

    // mobile nav toggle
    const navToggle = document.querySelector('.nav-toggle');
    const navList = document.querySelector('nav ul');
    if (navToggle) {
        navToggle.addEventListener('click', () => {
            navList.classList.toggle('open');
        });
    }

    // smooth scroll for nav
    document.querySelectorAll('nav a[href^="#"]').forEach(link => {
        link.addEventListener('click', e => {
            e.preventDefault();
            document.querySelector(link.getAttribute('href')).scrollIntoView({ behavior: 'smooth' });
            if (navList.classList.contains('open')) navList.classList.remove('open');
        });
    });

    // rotating expertise points
    const points = [""" + ", ".join(f'\"{pt}\"' for pt in expertise_points) + """ ];
    let idx = 0;
    const el = document.getElementById('rotating-point');
    function showNext() {
        el.textContent = '';
        el.classList.remove('typewriter');
        void el.offsetWidth; // reflow
        el.textContent = points[idx];
        el.classList.add('typewriter');
        idx = (idx + 1) % points.length;
    }
    showNext();
    setInterval(showNext, 3000);

    // fade-in on scroll
    const sections = document.querySelectorAll('section');
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('in-view');
                observer.unobserve(entry.target);
            }
        });
    }, { threshold: 0.1 });
    sections.forEach(sec => observer.observe(sec));
</script>
"""
