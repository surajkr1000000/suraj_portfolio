def build_experience():
    cards = []
    for exp in experience:
        # render details as bullet list if it's a list
        details_val = exp.get('details','')
        if isinstance(details_val, list):
            detail_items = "\n".join(f"                        <li>{d}</li>" for d in details_val)
            details_html = f"<ul>\n{detail_items}\n                    </ul>"
        elif isinstance(details_val, str) and ";" in details_val:
            # split exclusively on semicolons; preserve other whitespace inside segments
            parts = [seg.strip() for seg in details_val.split(";") if seg.strip()]
            detail_items = "\n".join(f"                        <li>{d}</li>" for d in parts)
            details_html = f"<ul>\n{detail_items}\n                    </ul>"
        elif isinstance(details_val, str) and "\n" in details_val:
            # split multiline string into bullets (fallback)
            parts = [line.strip() for line in details_val.splitlines() if line.strip()]
            detail_items = "\n".join(f"                        <li>{d}</li>" for d in parts)
            details_html = f"<ul>\n{detail_items}\n                    </ul>"
        else:
            details_html = f"<p>{details_val}</p>"
        cards.append(
            f"""
                <div class='card'>
                    <h4>{exp['company']}</h4>
                    <div class='designation'>{exp['designation']}</div>
                    <div class='dates'>{exp['dates']}</div>
                    <div class='details'>{details_html}</div>
                </div>"""
        )
    return f"""
<section id="experience" class="section container">
    <h2>Experience</h2>
    <div class="cards">
{''.join(cards)}
    </div>
</section>
"""


